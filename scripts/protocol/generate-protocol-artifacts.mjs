import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..", "..");
const contractPath = path.join(rootDir, "native", "protocol", "v1.contract.json");
const schemaPath = path.join(rootDir, "native", "protocol", "generated", "v1.schema.json");
const tsOutputPath = path.join(rootDir, "frontend", "packages", "engine-client", "src", "generated", "protocol.ts");
const checkOnly = process.argv.includes("--check");

const contract = JSON.parse(readFileSync(contractPath, "utf8"));

function stableJson(value) {
  return `${JSON.stringify(value, null, 2)}\n`;
}

function writeGenerated(filePath, content) {
  mkdirSync(path.dirname(filePath), { recursive: true });
  const normalized = content.endsWith("\n") ? content : `${content}\n`;
  const previous = safeRead(filePath);

  if (checkOnly) {
    if (previous !== normalized) {
      throw new Error(`Generated artifact is stale: ${path.relative(rootDir, filePath)}`);
    }
    return;
  }

  if (previous !== normalized) {
    writeFileSync(filePath, normalized, "utf8");
  }
}

function safeRead(filePath) {
  try {
    return readFileSync(filePath, "utf8");
  } catch {
    return null;
  }
}

const schema = {
  $schema: "https://json-schema.org/draft/2020-12/schema",
  $id: "https://sse.exed/protocol/v1.schema.json",
  title: "SSE ExEd Studio Control Protocol v1",
  type: "object",
  properties: {
    version: { const: contract.version },
    transport: {
      type: "object",
      properties: {
        mode: { const: contract.transport.mode },
        requestKind: { const: contract.transport.requestKind },
        responseKind: { const: contract.transport.responseKind },
        eventKind: { const: contract.transport.eventKind },
      },
      required: ["mode", "requestKind", "responseKind", "eventKind"],
      additionalProperties: false,
    },
    startupLifecycleStates: {
      type: "array",
      items: { enum: contract.startupLifecycleStates },
    },
    devParityFixtures: {
      type: "array",
      items: { enum: contract.devParityFixtures },
    },
    methods: {
      type: "array",
      items: { enum: contract.methods },
    },
    events: {
      type: "array",
      items: { enum: contract.events },
    },
  },
  required: ["version", "transport", "startupLifecycleStates", "devParityFixtures", "methods", "events"],
  additionalProperties: false,
};

const generatedTs = `/* eslint-disable */
// Generated by scripts/protocol/generate-protocol-artifacts.mjs

export const PROTOCOL_VERSION = ${JSON.stringify(contract.version)} as const;

export const REQUEST_METHODS = ${JSON.stringify(contract.methods, null, 2)} as const;
export type RequestMethod = (typeof REQUEST_METHODS)[number];

export const EVENT_NAMES = ${JSON.stringify(contract.events, null, 2)} as const;
export type EventName = (typeof EVENT_NAMES)[number];

export const STARTUP_LIFECYCLE_STATES = ${JSON.stringify(contract.startupLifecycleStates, null, 2)} as const;
export type StartupLifecycleState = (typeof STARTUP_LIFECYCLE_STATES)[number];

export const DEV_PARITY_FIXTURES = ${JSON.stringify(contract.devParityFixtures, null, 2)} as const;
export type DevParityFixtureId = (typeof DEV_PARITY_FIXTURES)[number];

export type JsonPrimitive = string | number | boolean | null;
export type JsonValue = JsonPrimitive | JsonValue[] | { [key: string]: JsonValue };
export type JsonObject = { [key: string]: JsonValue };

export interface RequestEnvelope<TMethod extends string = RequestMethod> {
  type: "request";
  id: JsonValue;
  method: TMethod;
  params: JsonObject;
}

export interface ResponseEnvelope<TResult extends JsonValue = JsonValue> {
  type: "response";
  id: JsonValue;
  ok: boolean;
  result?: TResult;
  error?: {
    code: string;
    message: string;
  };
}

export interface EventEnvelope<TEvent extends string = EventName> {
  type: "event";
  event: TEvent;
  payload: JsonObject;
}
`;

writeGenerated(schemaPath, stableJson(schema));
writeGenerated(tsOutputPath, generatedTs);
