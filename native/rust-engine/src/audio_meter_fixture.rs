fn interpolate_profile(seconds: f64, levels: &[u16]) -> f64 {
    let wrapped = seconds.rem_euclid(REAL_SPEECH_PROFILE_DURATION_SECONDS);
    let position = wrapped * REAL_SPEECH_PROFILE_HZ;
    let base_index = position.floor() as usize;
    let next_index = (base_index + 1) % levels.len();
    let fraction = position - base_index as f64;
    let current = f64::from(levels[base_index]) / f64::from(u16::MAX);
    let next = f64::from(levels[next_index]) / f64::from(u16::MAX);

    current + (next - current) * fraction
}

